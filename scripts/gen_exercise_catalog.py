def main():
    rows = load_rows()
    exercises = [row_to_exercise(r) for r in rows]
    validate_muscles(exercises)
    OUT_JSON.parent.mkdir(parents=True, exist_ok=True)
    OUT_JSON.write_text(json.dumps(exercises, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    write_muscle_sql()
    write_exercise_sql(exercises)
    write_ts()
    print(f"Wrote {len(exercises)} exercises -> {OUT_JSON.relative_to(ROOT)}")
    print(f"SQL -> {OUT_MUSCLE_SQL.relative_to(ROOT)}, {OUT_EX_SQL.relative_to(ROOT)}")
    print(f"Types -> {OUT_TS.relative_to(ROOT)}")


if __name__ == "__main__":
    main()
